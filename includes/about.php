<b>Stalker</b> allows you to find if a device is present (macaddress) by a sound alert or log file.
<br><br>
<b>Author</b>: xtr4nge [_AT_] gmail.com (@xtr4nge)
<br><br>
<div style="font-family: courier, monospace">
Command line usage: ./stalker.py -i INTERFACE -m MAC {options}
<br>
<br>Options:
<br>-i INTERFACE      &nbsp;&nbsp;        WLAN Interface (monitor mode required)
<br>-m MAC            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;        MAC address to be stalked
<br>-t TIME           &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;        Target gone time (default 10 seconds)
<br>-l LOG            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;        Log path (default ./stalker.log)
<br>-h                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;        This help
</div>